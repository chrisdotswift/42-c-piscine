#include <stdlib.h>

int		ft_ultimate_range(int **range, int min, int max)
{
	int *tab;
	int i;

	i = 0;
	if (min >= max)
	{
		*range = NULL;
		return (0);
	}
	if (!(tab = malloc(sizeof(int) * (max - min))))
	{
		return (-1);
	}
	*range = tab;
	while (min < max)
	{
		tab[i] = min;
		min++;
		i++;
	}
	return (i);
}
