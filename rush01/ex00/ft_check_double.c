int		ft_check_double(int tab[][4], int x, int y)
{
	int i;
	int j;

	i = 0;
	j = 0;
	if (x > 0)
	{
		while (i < x)
		{
			if (tab[y][x] == tab[y][i])
				return (0);
			i++;
		}
	}
	if (y > 0)
	{
		while (j < y)
		{
			if (tab[y][x] == tab[j][x])
				return (0);
			j++;
		}
	}
	return (1);
}
